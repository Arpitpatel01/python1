#break control
for i in "hello world":
    if i=="w":
       break
    print("current:" ,i)
    print("bye")

#continue control 
for i in "python":
    if i=="y":
        continue 
    print("current update:",i)
    print("bye") 

#pass control
for i in "python":
    if i == "o":
        pass
    print("current up:",i)
    print("bye")